const fallbackLanguage: SupportedLanguage = 'en';

const resolveSupportedLanguage = (locale?: string): SupportedLanguage => {
  const lower = String(locale || '').toLowerCase();
  if (lower.startsWith('de')) return 'de';
  if (lower.startsWith('fr')) return 'fr';
  if (lower.startsWith('es')) return 'es';
  if (lower.startsWith('zh')) return 'zh-Hans';
  if (lower.startsWith('hi')) return 'hi';
  return 'en';
};

export const getSystemLanguage = (): SupportedLanguage => {
  try {
    const locale = Intl.DateTimeFormat().resolvedOptions().locale;
    return resolveSupportedLanguage(locale);
  } catch (_error) {
    return fallbackLanguage;
  }
};

export const translate = (
  language: SupportedLanguage,
  key: string,
  fallbackText: string,
  vars?: Record<string, string | number>,
): string => {
  const source = dictionaries[language]?.[key] || dictionaries[fallbackLanguage]?.[key] || fallbackText;
  if (!vars) {
    return source;
  }
  return Object.entries(vars).reduce((result, [name, value]) => (
    result.split(`{${name}}`).join(String(value))
  ), source);
};

